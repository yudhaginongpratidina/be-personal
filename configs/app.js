// ===============================================================================
// DEPENDENCIES
// ===============================================================================
import express from "express";
import cors from "cors";


// ===============================================================================
// ROUTES
// ===============================================================================
import API from "../routes/api.js";


// ===============================================================================
// INITIALIZATION
// ===============================================================================
const APP = express();


// ===============================================================================
// MIDDLEWARE
// ===============================================================================
APP.use(cors());
APP.use(express.json());
APP.use(express.urlencoded({ extended: true }));


// ===============================================================================
// ROUTE DECLARATIONS
// ===============================================================================
APP.use("/api", API);


// ===============================================================================
// EXPORT
// ===============================================================================
export default APP;